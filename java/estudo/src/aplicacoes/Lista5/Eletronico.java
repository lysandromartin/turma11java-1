package Lista5;


public class Eletronico {
	
	//ATRIBUTOS
	public String nome;
	public String marca;
	public String modelo;
	public int anoFabricacao;
	public int voltagem;
	public double preco;
	

	//CONSTRUTOR
	public Eletronico (String nome, String marca, String modelo, int anoFabricacao, int voltagem, double preco) {
		this.nome = nome;
		this.marca = marca;
		this.modelo = modelo;
		this.anoFabricacao = anoFabricacao;
		this.voltagem = voltagem;
		this.preco = preco;
		
	}
	
	
}
